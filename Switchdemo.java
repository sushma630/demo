public class Switchdemo{
	
	
	public static void main(String[] args)
	{
		int i=10;
		
		switch(i)
		{
			case 1: System.out.println("the value is"+10);
			break;
			
			case 2: System.out.println("the value is"+20);
			break;
			
			case 3: System.out.println("the value is"+30);
			break;
			
			default:System.out.println("the value is"+ "nothing");
			
		}
		char colour='b';
		switch(colour)
		{
			case 'r': System.out.println("the value is"+ "red");
			break;
			case 'b': System.out.println("the value is"+ "blue");
			break;
			default:System.out.println("the value is"+ "white");
			}
			
			
		}
	}